export const applyVariables = (
  value: string,
  variables?: Record<string, string | number>,
): string => {
  if (!variables) return value;
  return Object.entries(variables).reduce<string>(
    (acc, [placeholder, v]) => acc.replace(`{{${placeholder}}}`, String(v)),
    value,
  );
};

